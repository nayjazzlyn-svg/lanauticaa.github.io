function closeAlbumDD(px) {
  var l = document.getElementById(px + 'AlbumList'),
    a = document.getElementById(px + 'AlbumArrow'),
    t = document.getElementById(px + 'AlbumTrigger');
  if (l) l.classList.remove('open');
  if (a) a.classList.remove('open');
  if (t) t.classList.remove('dd-open');
  ddState[px].isOpen = false;
}

function toggleAlbumDD(px) {
  if (ddState[px].isOpen) closeAlbumDD(px);
  else openAlbumDD(px);
}

function setupAlbumDD(px) {
  var search = document.getElementById(px + 'AlbumSearch'),
    arrow = document.getElementById(px + 'AlbumArrow'),
    list = document.getElementById(px + 'AlbumList');

  list.addEventListener('mousedown', function (e) {
    if (e.target.closest('.album-dd-item')) e.preventDefault();
  });

  arrow.addEventListener('click', function (e) {
    e.stopPropagation();
    toggleAlbumDD(px);
    search.focus();
  });

  search.addEventListener('focus', function () {
    this.select();
    openAlbumDD(px);
  });

  search.addEventListener('input', function () {
    renderAlbumDDList(px);
  });

  list.addEventListener('click', function (e) {
    var it = e.target.closest('.album-dd-item');
    if (!it || !it.dataset.value) return;
    var v = it.dataset.value;
    if (v === '__new__') {
      ddState[px].value = '__new__';
      ddState[px].newKey = it.dataset.newKey || '';
      search.value = ddState[px].newKey;
    } else {
      ddState[px].value = v;
      ddState[px].newKey = '';
      var f = findFolder(v);
      search.value = f ? f.name : it.textContent.trim();
    }
    closeAlbumDD(px);
    onAlbumSelect(px === 'g' ? 'add' : 'edit');
  });

  search.addEventListener('keydown', function (e) {
    if (e.key === 'Escape') {
      closeAlbumDD(px);
      search.blur();
      return;
    }
    if (e.key === 'Enter') {
      e.preventDefault();
      var fi = list.querySelector('.album-dd-item');
      if (fi) fi.click();
    }
  });

  search.addEventListener('blur', function () {
    setTimeout(function () {
      if (!ddState[px].isOpen) return;
      closeAlbumDD(px);
      var tt = search.value.trim();
      if (!tt) {
        ddState[px].value = '';
        ddState[px].newKey = '';
        return;
      }
      var cv = ddState[px].value;
      if (cv && cv !== '__new__') {
        var f = findFolder(cv);
        if (f && f.name === tt) return;
      }
      if (cv === '__new__' && ddState[px].newKey === tt) return;
      var catId = px === 'g' ? 'gCat' : 'egCat';
      var cat = document.getElementById(catId).value;
      var all = getAlbumItems(cat, '');
      var exact = all.find(function (i) {
        return i.name.toLowerCase() === tt.toLowerCase();
      });
      if (exact) {
        ddState[px].value = exact.value;
        ddState[px].newKey = '';
        search.value = exact.name;
      } else {
        ddState[px].value = '__new__';
        ddState[px].newKey = tt;
      }
      onAlbumSelect(px === 'g' ? 'add' : 'edit');
    }, 150);
  });
}

