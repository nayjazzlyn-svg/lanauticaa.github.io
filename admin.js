function getNextFileNumber(cat, al) {
  var mx = 0;
  var pf = cat === 'lana' ? 'lana (' : 'lanautica (';
  galeriData.forEach(function (r) {
    if (r.folder === cat && r.album === al && r.src) {
      var fn = r.src.split('/').pop();
      if (fn.toLowerCase().startsWith(pf.toLowerCase())) {
        var m = fn.match(/\((\d+)\)/);
        if (m) {
          var n = parseInt(m[1], 10);
          if (n > mx) mx = n;
        }
      }
    }
  });
  return mx + 1;
}

function generateFilenames() {
  var cat = document.getElementById('gCat').value;
  var s = parseInt(document.getElementById('gFileStart').value, 10);
  var e = parseInt(document.getElementById('gFileEnd').value, 10);
  if (isNaN(s) || isNaN(e) || s <= 0 || e <= 0 || s > e) {
    document.getElementById('gSrc').value = '';
    return;
  }
  var pf = cat === 'lana' ? 'lana' : 'lanautica';
  var lines = [];
  for (var i = s; i <= e; i++) {
    lines.push(pf + ' (' + i + ').jpg');
  }
  document.getElementById('gSrc').value = lines.join('\n');
}

