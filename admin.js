function setupNamaDD(px) {
  var search = document.getElementById(px + 'NamaSearch'),
    arrow = document.getElementById(px + 'NamaArrow'),
    list = document.getElementById(px + 'NamaList');

  list.addEventListener('mousedown', function (e) {
    if (e.target.closest('.album-dd-item')) e.preventDefault();
  });

  arrow.addEventListener('click', function (e) {
    e.stopPropagation();
    toggleNamaDD(px);
    search.focus();
  });

  search.addEventListener('focus', function () {
    this.select();
    openNamaDD(px);
  });

  search.addEventListener('input', function () {
    renderNamaDDList(px);
  });

  list.addEventListener('click', function (e) {
    var it = e.target.closest('.album-dd-item');
    if (!it || !it.dataset.value) return;
    var v = it.dataset.value;
    if (v === '__new__') {
      namaDDState[px].value = '__new__';
      search.value = it.dataset.newKey || '';
    } else {
      namaDDState[px].value = v;
      search.value = v;
    }
    closeNamaDD(px);
  });

  search.addEventListener('keydown', function (e) {
    if (e.key === 'Escape') {
      closeNamaDD(px);
      search.blur();
      return;
    }
    if (e.key === 'Enter') {
      e.preventDefault();
      var fi = list.querySelector('.album-dd-item');
      if (fi) fi.click();
    }
  });

  search.addEventListener('blur', function () {
    setTimeout(function () {
      if (!namaDDState[px].isOpen) return;
      closeNamaDD(px);
      var tt = search.value.trim();
      if (!tt) {
        namaDDState[px].value = '';
        return;
      }
      namaDDState[px].value = tt;
    }, 150);
  });
}

